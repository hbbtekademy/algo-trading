	for k, v := range indices {
		instruments[k] = v
	}
	mid50 := GetNiftyMid50()
	for k, v := range mid50 {
		instruments[k] = v
	}
	small50 := GetNiftySmall50()
	for k, v := range small50 {
		instruments[k] = v
	}
	startup := GetNiftyStartup()
	for k, v := range startup {
		instruments[k] = v
	}
	return instruments
